package com.company.model;

public class Customer {

	public String getCustomerId() {
		return CustomerId;
	}

	public void setCustomerId(String customerId) {
		CustomerId = customerId;
	}

	public String getCustomerFirstName() {
		return CustomerFirstName;
	}

	public void setCustomerFirstName(String customerFirstName) {
		CustomerFirstName = customerFirstName;
	}

	public String getProposalApprover() {
		return ProposalApprover;
	}

	public void setProposalApprover(String proposalApprover) {
		ProposalApprover = proposalApprover;
	}

	public String getProposalCreator() {
		return ProposalCreator;
	}

	public void setProposalCreator(String proposalCreator) {
		ProposalCreator = proposalCreator;
	}

	public String getCustomerCity() {
		return CustomerCity;
	}

	public void setCustomerCity(String customerCity) {
		CustomerCity = customerCity;
	}

	public String getCustomerCountry() {
		return CustomerCountry;
	}

	public void setCustomerCountry(String customerCountry) {
		CustomerCountry = customerCountry;
	}

	public String getCustomerLanguage() {
		return CustomerLanguage;
	}

	public void setCustomerLanguage(String customerLanguage) {
		CustomerLanguage = customerLanguage;
	}

	public String getCustomerCategory() {
		return CustomerCategory;
	}

	public void setCustomerCategory(String customerCategory) {
		CustomerCategory = customerCategory;
	}

	public String getCustomerLastName() {
		return CustomerLastName;
	}

	public void setCustomerLastName(String customerLastName) {
		CustomerLastName = customerLastName;
	}

	private String CustomerId;

	private String CustomerFirstName;
	
	private String ProposalApprover;
	
	private String ProposalCreator;
	
	private String CustomerCity;
	
	private String CustomerCountry;
	
	private String CustomerLanguage;
	
	private String CustomerCategory;
	
	private String CustomerLastName;
		
}
